# ============================================================
# AI 辅助 R 语言 · dplyr 管道操作
# 场景：电商销售数据分析 — 从原始数据到洞察，一条管道搞定
# 运行：Rscript 04_R_dplyr管道操作.R
# ============================================================

library(dplyr)
library(tidyr)
library(stringr)

# ============================================================
# 1. 准备数据：电商平台订单
# ============================================================
set.seed(42)
orders <- data.frame(
  order_id   = 1001:1020,
  customer   = c("张三","李四","王五","赵六","张三","李四","王五","赵六",
                 "张三","李四","王五","赵六","张三","李四","王五","赵六",
                 "张三","李四","王五","赵六"),
  product    = c("手机","耳机","手机壳","充电器","手机","耳机","手机壳","充电器",
                 "手机","耳机","手机壳","充电器","手机","耳机","手机壳","充电器",
                 "手机","耳机","手机壳","充电器"),
  category   = c("电子产品","配件","配件","配件","电子产品","配件","配件","配件",
                 "电子产品","配件","配件","配件","电子产品","配件","配件","配件",
                 "电子产品","配件","配件","配件"),
  amount     = c(2999, 199, 29, 59, 3299, 249, 35, 69,
                 2799, 179, 25, 55, 3499, 299, 39, 65,
                 2599, 159, 19, 49),
  quantity   = c(1, 2, 3, 1, 1, 1, 2, 1, 1, 3, 4, 2, 1, 2, 1, 1, 1, 4, 5, 3),
  date       = as.Date(c("2024-01-05","2024-01-08","2024-02-12","2024-02-20",
                         "2024-03-03","2024-03-15","2024-04-01","2024-04-10",
                         "2024-05-06","2024-05-18","2024-06-02","2024-06-14",
                         "2024-07-09","2024-07-21","2024-08-03","2024-08-15",
                         "2024-09-01","2024-09-13","2024-10-07","2024-10-19")),
  stringsAsFactors = FALSE
)

cat("=== 原始数据（前 8 行）===\n")
print(head(orders, 8))

# ============================================================
# 2. 管道操作：一条 %>% 链路完成全部分析
# ============================================================

cat("\n\n=== 🚀 管道分析：从原始数据到业务洞察 ===\n\n")

result <- orders %>%
  # 步骤1：添加月份列（数据预处理）
  mutate(month = format(date, "%Y-%m")) %>%
  
  # 步骤2：过滤掉金额太小的小额订单（数据清洗）
  filter(amount >= 10) %>%
  
  # 步骤3：按产品和月份分组汇总
  group_by(category, month) %>%
  summarise(
    订单数   = n(),
    总销售额  = sum(amount * quantity),
    平均单价  = mean(amount),
    .groups  = "drop"
  ) %>%
  
  # 步骤4：按销售额降序排列
  arrange(desc(总销售额)) %>%
  
  # 步骤5：添加排名列
  mutate(排名 = row_number())

cat("📊 按品类×月份汇总（管道输出）:\n")
print(result, n = 20)

# ============================================================
# 3. 拆解：每一步的作用
# ============================================================

cat("\n\n=== 🔍 管道拆解：每一步都在做什么 ===\n")

# 只用电子产品数据演示单个步骤
orders %>%
  filter(category == "电子产品") %>%
  mutate(total = amount * quantity) %>%
  select(order_id, customer, product, total) %>%
  cat("\n电子产品订单（filter → mutate → select）:\n")
print(.)

# ============================================================
# 4. 高级管道：多表关联 + 窗口函数
# ============================================================

cat("\n\n=== 🧠 高级管道：客户分层分析 ===\n")

# 模拟客户信息表
customers <- data.frame(
  customer = c("张三","李四","王五","赵六"),
  city     = c("北京","上海","广州","深圳"),
  level    = c("VIP","普通","VIP","普通"),
  stringsAsFactors = FALSE
)

# 多表关联 + 窗口函数 + 汇总 全部在一条管道
orders %>%
  inner_join(customers, by = "customer") %>%
  group_by(customer, city, level) %>%
  summarise(
    购买次数 = n(),
    总消费   = sum(amount * quantity),
    .groups  = "drop"
  ) %>%
  group_by(level) %>%
  mutate(
    组内排名 = rank(desc(总消费)),
    占比     = scales::percent(总消费 / sum(总消费), accuracy = 0.1)
  ) %>%
  arrange(level, desc(总消费)) %>%
  cat("\n客户价值分层（inner_join → summarise → 窗口排名）:\n")
print(.)

