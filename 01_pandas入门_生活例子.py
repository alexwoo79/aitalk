"""
=== AI 辅助学习 · 入门篇 ===
用生活中的例子理解 pandas 数据筛选

场景：你是一家奶茶店的店长，用一个表格记录每天的销售数据。
现在你用手里的「销售小本本」来做各种查询——pandas 就是你的电子小本本。
"""

import pandas as pd

# ============================================================
# 1. 准备数据：奶茶店一周销售记录
# ============================================================
data = {
    "日期":       ["周一","周二","周三","周四","周五","周六","周日"],
    "饮品":       ["珍珠奶茶","杨枝甘露","柠檬茶","珍珠奶茶","红豆奶茶","杨枝甘露","珍珠奶茶"],
    "杯数":       [30, 25, 40, 20, 35, 50, 45],
    "单价(元)":   [15, 18, 12, 15, 14, 18, 15],
    "是否加糖":   ["是","否","是","是","否","是","否"],
}
df = pd.DataFrame(data)

print("=" * 60)
print("📋 奶茶店一周销售表")
print("=" * 60)
print(df.to_string(index=False))

# ============================================================
# 2. 用生活类比理解筛选
# ============================================================

# --- 类比 1：像翻菜单一样 —— 筛选特定饮品 ---
# 「老板，把所有珍珠奶茶的单子挑出来」
print("\n🥤 筛选：找出所有「珍珠奶茶」的订单")
zhenzhu = df[df["饮品"] == "珍珠奶茶"]
print(zhenzhu.to_string(index=False))
# 原理：df["饮品"]=="珍珠奶茶" 会对每一行问「你是珍珠奶茶吗？」
#       回答 True/False，然后 df[True/False] 只保留 True 的行。
#       就像你在本子上一行行看，是珍珠奶茶就打勾，最后只看打勾的行。

# --- 类比 2：像设最低消费 —— 数值比较筛选 ---
# 「把卖了超过 30 杯的挑出来，看看哪些是爆款」
print("\n🔥 筛选：杯数 > 30（爆款饮品）")
baokuan = df[df["杯数"] > 30]
print(baokuan.to_string(index=False))

# --- 类比 3：像同时满足多个条件 —— 组合筛选 ---
# 「加糖的珍珠奶茶有哪些天？」
print("\n🍬 组合筛选：饮品是珍珠奶茶 AND 加糖")
tiaojian = (df["饮品"] == "珍珠奶茶") & (df["是否加糖"] == "是")
print(df[tiaojian].to_string(index=False))
# 原理：& 就是「并且」。(A) & (B) 必须两个都满足。
#       | 就是「或者」。(A) | (B) 满足一个就行。
#       就像你同时提了两个要求，助理帮你交叉比对。

# --- 类比 4：像查字典 —— 用 .query() 写「人话」---
print("\n📖 使用 .query() 写法（更接近自然语言）")
result = df.query("杯数 >= 30 and 饮品 == '珍珠奶茶'")
print(result.to_string(index=False))

# --- 类比 5：像筛选列 —— 只看关心的字段 ---
print("\n📊 只看「饮品」和「杯数」两列")
print(df[["饮品", "杯数"]].to_string(index=False))

# ============================================================
# 3. 一句话总结
# ============================================================
print("\n" + "=" * 60)
print("💡 核心类比：")
print("   df[条件]   →  就像在纸质表格上，用荧光笔标出符合条件的行")
print("   & 和 |     →  「并且」和「或者」")
print("   .query()   →  直接用人话提问")
print("=" * 60)
