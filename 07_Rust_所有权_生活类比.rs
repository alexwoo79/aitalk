// 📋 Copy 与 Clone：复印 vs 克隆
// ============================================================
fn demo_copy_vs_clone() {
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    println!("📋 Copy vs Clone：免费复印 vs 付费克隆");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");

    // Copy 类型（栈上数据，自动复制）：类比便利贴，撕一张给你不费劲
    let x = 42;
    let y = x; // x 被「复印」了，不是移动！
    println!("✅ [Copy] x={}, y={} — 整数是 Copy 类型，x 还能用", x, y);
    println!("   → 类比：便利贴上的数字，抄一份给你，原件还在\n");

    // Clone 类型（堆上数据，显式复制）：类比复印整本书，要花钱花时间
    let book1 = String::from("📕 厚厚的一本书");
    let book2 = book1.clone(); // 必须显式 clone
    println!("✅ [Clone] {} | {} — 两本独立的书", book1, book2);
    println!("   → 类比：整本书复印，费时费力，但得到两本独立的书\n");

    // 哪些是 Copy 类型？
    let a: i32 = 10; // ✅ 整数
    let b: f64 = 3.14; // ✅ 浮点数
    let c: bool = true; // ✅ 布尔
    let d: char = '🦀'; // ✅ 字符
    let e: (&str, i32) = ("tuple", 5); // ✅ 全部是 Copy 的元组
    let _ = (a, b, c, d, e); // 全部按位复制，都能继续用
    println!("✅ 以上类型都是 Copy：整数、浮点、布尔、字符、纯 Copy 元组\n");

    println!("╔══════════════════════════════════════════════════════╗");
    println!("║  🎯 核心心法：                                       ║");
    println!("║  所有权 = 房子产权证（一个房子一个主人）              ║");
    println!("║  移动   = 搬家（旧地址失效）                          ║");
    println!("║  借用   = 图书馆借书（看完归还，书还是图书馆的）       ║");
    println!("║  生命周期 = 电影票有效期（票不能比电影活得更久）      ║");
    println!("║  Copy   = 便利贴数字（撕一份，原件还在）              ║");
    println!("║  Clone  = 复印全书（费时费力，得到两本）              ║");
    println!("╚══════════════════════════════════════════════════════╝");
}
