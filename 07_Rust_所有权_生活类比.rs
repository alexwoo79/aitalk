// ⏳ 生命周期：确保引用不会比被引用的东西活得更久
// ============================================================
fn demo_lifetimes() {
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    println!("⏳ 生命周期：引用不能比原件活得久");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");

    // 类比：电影票不能比电影放映时间还有效
    // 类比：借书证不能比图书馆关门后还能使用

    let result;
    {
        let short_lived = String::from("🦋 短暂的生命");
        result = pick_longer("永久的", &short_lived);
        println!("✅ 两者中较长的是：{}", result);
    }
    // short_lived 已释放，但 result 的内容是从返回值拷出来的 String，所以 OK
    println!("   → 类比：电影散场了，但票根（String）还在你手里\n");

    // 静态生命周期：和程序活得一样久
    let motto: &'static str = "Rust 让系统编程更安全";
    println!("✅ 静态字符串（'static）：'{}' — 永远不会失效", motto);
    println!("   → 类比：刻在石头上的字，永远存在\n");
}

// 生命周期标注：'a 表示「两个引用活得一样久的那段时间」
fn pick_longer<'a>(a: &'a str, b: &'a str) -> &'a str {
    // 编译器通过 'a 确保返回的引用不会比 a 或 b 活得更久
    if a.len() > b.len() {
        a
    } else {
        b
    }
}

