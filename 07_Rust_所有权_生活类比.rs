// 📚 借用与引用：就像图书馆借书
// ============================================================
fn demo_borrowing() {
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    println!("📚 借用：只借不送，看完归还");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");

    // --- 不可变借用（&）：只读借阅 ---
    let library_book = String::from("《Rust 权威指南》");
    read_book(&library_book); // 借出去读
    read_book(&library_book); // 再借一次（可以多次借阅）
    println!("✅ 书还在我这里：{}\n", library_book); // 所有者还能用！

    // --- 可变借用（&mut）：借来修改 ---
    let mut notebook = String::from("📓 空白笔记本");
    println!("原内容：{}", notebook);
    write_in_notebook(&mut notebook); // 借去写字
    println!("✅ 笔记本被写了字，归还后：{}\n", notebook);

    // --- 切片的借用：只借其中几页 ---
    let textbook = String::from("第一章 基础 | 第二章 进阶 | 第三章 高级");
    let chapter1 = &textbook[0..18]; // 只借第一章
    println!("✅ 只借阅第一章：{}", chapter1);
    println!("   全书还在我这里：{}\n", textbook);
}

fn read_book(book: &String) {
    // &String 表示「只读借用」，我不会修改也不会带走
    println!("   📖 正在阅读：{}（看完就还）", book);
}

fn write_in_notebook(nb: &mut String) {
    // &mut String 表示「我借来修改一下」
    nb.push_str("\n2024年 Rust 学习笔记：所有权是 Rust 的灵魂特性");
}

