// 🚚 移动语义：就像搬家
// ============================================================
fn demo_move_semantics() {
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    println!("🚚 移动语义：所有权转移就像搬家");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");

    // --- 移动（Move）：家具从老房子搬到新房子 ---
    let old_house = String::from("沙发");
    let new_house = old_house; // 沙发「移动」到新房子了！
                               // println!("{}", old_house); // ❌ 老房子已经没有沙发了
    println!("✅ [移动] 沙发从老房子搬到了新房子：{}", new_house);
    println!("   → 类比：搬家后，旧地址不再有效，只有新地址能找到家具\n");

    // --- 克隆（Clone）：复制一套一模一样的家具 ---
    let original = String::from("名画《星空》");
    let copy = original.clone(); // 克隆一份（花钱复制）
    println!("✅ [克隆] 原画：{} | 复制品：{}", original, copy);
    println!("   → 类比：花成本复制一份，原件和复制品独立存在\n");

    // --- 函数传参：所有权也转移 ---
    let gift = String::from("🎁 生日礼物");
    deliver_gift(gift); // 礼物送出去了
                        // println!("{}", gift); // ❌ 礼物已经不属于你了
    println!("   → 类比：送出礼物后，你不再拥有它\n");

    // --- 返回所有权：物归原主 ---
    let borrowed_item = String::from("🔧 扳手");
    let returned = borrow_and_return(borrowed_item);
    println!("✅ [归还] 扳手借出后归还：{}", returned);
    println!("   → 类比：借工具用完归还，所有权又回到你手中\n");
}

fn deliver_gift(g: String) {
    println!("   🎀 礼物「{}」已签收", g);
    // g 在这里被释放（消耗掉了）
}

fn borrow_and_return(item: String) -> String {
    println!("   📥 借入：{}", item);
    item // 归还所有权
}

