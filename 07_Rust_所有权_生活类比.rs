// 🏠 类比总览：所有权就像房产证
// ============================================================
// 规则1: 每个值有且只有一个所有者（一套房只有一个房产证上的名字）
// 规则2: 所有者离开作用域，值被释放（房子拆迁，房产作废）
// 规则3: 任何时候只能有一个可变引用或多个不可变引用
//        （要么多人同时「看」房，要么一人「装修」房，不能同时）

fn demo_ownership_rules() {
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
    println!("📋 规则演示：所有权三大规则");
    println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");

    // --- 规则 1: 一个值一个所有者 ---
    // 类比：一本书只能属于一个人
    {
        let book = String::from("《Rust 编程之道》"); // book 拥有这本书
        println!(
            "✅ [规则1] {} 属于 book 变量（就像一本书只有一位主人）",
            book
        );
    }
    // book 离开作用域，书被归还/销毁
    // println!("{}", book); // ❌ 编译错误：book 已经不存在了

    // --- 规则 2: 离开作用域自动释放 ---
    // 类比：快递包裹签收后，快递单失效
    {
        let package = String::from("📦 重要文件");
        println!("✅ [规则2] 包裹「{}」在 {} 内有效", package, "{} 作用域");
    } // package 在此被 drop，就像包裹拆开后盒子扔掉

    // --- 规则 3a: 多个不可变引用共存（只读共享）---
    // 类比：图书馆的书，多人可以同时借阅（只读）
    let book = String::from("《三体》");
    let reader1 = &book; // 读者 1 借阅
    let reader2 = &book; // 读者 2 也借阅（同时借阅 OK）
    let reader3 = &book; // 读者 3 也借阅
    println!(
        "✅ [规则3a] 多人同时「看」书：{} | {} | {}",
        reader1, reader2, reader3
    );
    println!("   → 类比：图书馆的书，多人可以同时翻阅（共享只读引用）\n");

    // --- 规则 3b: 可变引用独占 ---
    // 类比：房子装修时，不能同时有人来看房
    let mut house = String::from("🏠 毛坯房");
    let decorator = &mut house; // 装修队进场，独占访问
    decorator.push_str(" → 精装修完成");
    println!("✅ [规则3b] 装修完成后的房子：{}", decorator);
    println!("   → 类比：装修时只能一个团队施工（独占可变引用）\n");
    // 装修队离开后，别人才能来看房

    println!(
        "💡 一句话总结：Rust 编译器就像一个严格的物业经理，\n\
               确保任何时候都不会出现「同时在装修又有人在看房」的危险情况。\n"
    );
}

